// In JS, values are considered truthy if they are not falsy. Falsy values include false, 0, "", null, undefined, and NaN. All other values are considered truthy. This means that if a value is not explicitly false, it is considered true. For example, the following values are all considered truthy:

// true
// 42
// "hello"
// "0"

// The following values are all considered falsy:

// false
// 0
// ""
